"""Build the dataset overview table across all three size tiers.

Owner: Kirill. Scans the small / medium / large repository tiers and emits:
  * datasets/dataset_overview.csv          (one row per repo + a 'group' column)
  * reports/tables/dataset_groups.tex      (grouped LaTeX table with subtotals)

Columns: repository, files, chunks, issues, expected_labels.
- ``chunks`` is the AST-aware chunk count. For the synthetic scale tiers each
  top-level definition is exactly one chunk; for the small mixed-language tier it
  is a language-aware definition-count estimate.
- ``expected_labels`` is the number of ground-truth ``expected_files`` annotations
  across the repository's issues.

Standalone: stdlib + PyYAML only.
"""

from __future__ import annotations

import csv
import json
import re
from pathlib import Path

REPO_ROOT = Path(__file__).resolve().parent.parent
TIERS = [
    ("small", REPO_ROOT / "datasets" / "repositories"),
    ("medium", REPO_ROOT / "datasets" / "medium_repositories"),
    ("large", REPO_ROOT / "datasets" / "large_repositories"),
]
CODE_EXT = {".py", ".go", ".js", ".jsx", ".ts", ".tsx", ".vue", ".rs", ".java", ".cpp", ".c"}
CSV_OUT = REPO_ROOT / "datasets" / "dataset_overview.csv"
TEX_OUT = REPO_ROOT / "reports" / "tables" / "dataset_groups.tex"


def count_defs(path: Path, text: str) -> int:
    ext = path.suffix.lower()
    if ext == ".py":
        return len(re.findall(r"(?m)^[ \t]*(?:async\s+def|def|class)\b", text))
    if ext == ".go":
        return len(re.findall(r"(?m)^func\b", text)) + len(re.findall(r"(?m)^type\b", text))
    if ext == ".rs":
        return len(re.findall(r"(?m)^[ \t]*(?:pub\s+)?(?:fn|impl|struct|enum|trait)\b", text))
    if ext in {".js", ".jsx", ".ts", ".tsx", ".vue"}:
        return (
            len(re.findall(r"\bfunction\b", text))
            + len(re.findall(r"=>", text))
            + len(re.findall(r"\bclass\s+\w", text))
            + len(re.findall(r"\binterface\s+\w", text))
            + len(re.findall(r"(?m)^\s*type\s+\w+\s*=", text))
        )
    return len(re.findall(r"(?m)^[ \t]*(?:def|class|function|func|fn)\b", text))


def repo_stats(repo_dir: Path) -> dict:
    code_files = [
        p for p in repo_dir.rglob("*")
        if p.is_file() and p.suffix.lower() in CODE_EXT and "issues.jsonl" not in p.name
    ]
    chunks = 0
    for p in code_files:
        try:
            chunks += count_defs(p, p.read_text(encoding="utf-8"))
        except (UnicodeDecodeError, OSError):
            continue
    issues = [
        json.loads(line)
        for line in (repo_dir / "issues.jsonl").read_text(encoding="utf-8").splitlines()
        if line.strip()
    ]
    expected = sum(len(i.get("expected_files", [])) for i in issues)
    return {
        "repository": repo_dir.name,
        "files": len(code_files),
        "chunks": chunks,
        "issues": len(issues),
        "expected_labels": expected,
    }


def esc(text: str) -> str:
    return str(text).replace("_", r"\_")


def main() -> int:
    rows: list[dict] = []
    groups: dict[str, list[dict]] = {}
    for group, base in TIERS:
        if not base.exists():
            groups[group] = []
            continue
        repos = sorted(d for d in base.iterdir() if d.is_dir() and (d / "repo.yml").exists())
        stats = [repo_stats(d) for d in repos]
        for s in stats:
            s["group"] = group
        groups[group] = stats
        rows.extend(stats)

    # CSV
    CSV_OUT.parent.mkdir(parents=True, exist_ok=True)
    with CSV_OUT.open("w", newline="", encoding="utf-8") as fh:
        writer = csv.DictWriter(
            fh, fieldnames=["group", "repository", "files", "chunks", "issues", "expected_labels"]
        )
        writer.writeheader()
        for r in rows:
            writer.writerow({k: r[k] for k in writer.fieldnames})

    # LaTeX grouped table
    TEX_OUT.parent.mkdir(parents=True, exist_ok=True)
    labels = {"small": "Small ($<$50k chunks)", "medium": "Medium (50k chunks)",
              "large": "Large (500k chunks)"}
    lines = [
        "% Auto-generated by scripts/build_dataset_table.py -- do not edit by hand.",
        r"\begin{tabular}{l r r r r}",
        r"\toprule",
        r"Repository & Files & Chunks & Issues & Exp.\ labels \\",
    ]
    g_files = g_chunks = g_issues = g_exp = 0
    for group, base in TIERS:
        stats = groups.get(group, [])
        if not stats:
            continue
        lines.append(r"\midrule")
        lines.append(
            rf"\multicolumn{{5}}{{l}}{{\textbf{{{labels[group]}}} -- {len(stats)} repositories}} \\"
        )
        lines.append(r"\midrule")
        sf = sc = si = se = 0
        for s in stats:
            lines.append(
                rf"\texttt{{{esc(s['repository'])}}} & {s['files']} & {s['chunks']} & "
                rf"{s['issues']} & {s['expected_labels']} \\"
            )
            sf += s["files"]; sc += s["chunks"]; si += s["issues"]; se += s["expected_labels"]
        lines.append(
            rf"\multicolumn{{1}}{{r}}{{\emph{{subtotal}}}} & {sf} & {sc} & {si} & {se} \\"
        )
        g_files += sf; g_chunks += sc; g_issues += si; g_exp += se
    lines += [
        r"\midrule",
        rf"\textbf{{Total}} & \textbf{{{g_files}}} & \textbf{{{g_chunks}}} & "
        rf"\textbf{{{g_issues}}} & \textbf{{{g_exp}}} \\",
        r"\bottomrule",
        r"\end{tabular}",
    ]
    TEX_OUT.write_text("\n".join(lines) + "\n", encoding="utf-8")

    print(f"repos: {len(rows)}  total chunks: {g_chunks}  files: {g_files}  "
          f"issues: {g_issues}  expected_labels: {g_exp}")
    for group, base in TIERS:
        st = groups.get(group, [])
        print(f"  {group}: {len(st)} repos, chunks={sum(s['chunks'] for s in st)}")
    print(f"wrote {CSV_OUT.relative_to(REPO_ROOT)} and {TEX_OUT.relative_to(REPO_ROOT)}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
